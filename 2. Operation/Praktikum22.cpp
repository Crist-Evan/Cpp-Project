#include <iostream>
using namespace std;

int main() {

    int a;
    int b;

    cout << "Masukan nilai a = ";
    cin >> a;
    cout << "Masukan nilai b = ";
    cin >> b;

    cout << "a + b = " << (a + b) << endl;
    cout << "a - b = " << (a - b) << endl;
    cout << "a * b = " << (a * b) << endl;
    cout << "a / b = " << (a / b) << endl;
    cout << "a % b = " << (a % b) << endl;

    return 0;
}
